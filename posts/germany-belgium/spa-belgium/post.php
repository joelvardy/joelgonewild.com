<p>During the race we were sat in the Eau Rouge grandstand, once the race had finished we spent around two hours walking most of the track (and relaxing in the sun) before heading back to the campsite.</p>

<p>The campsite clearly gave us a lift to the nearest town to catch a bus back to Verviers, where we got a train to Brussels, and after spending a few hours drinking more Belgian beer and eating waffles we got onto the Eurostar.. Which was delayed, causing us to run for our train back to Sheffield.</p>
