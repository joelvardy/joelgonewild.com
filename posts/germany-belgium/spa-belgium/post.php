<p>On the 21st we got breakfast in Cologne, aware that it might be the last decent meal we would get for a few days.</p>

<p>We got a train from Cologne to a sleepy station in Verviers, where we expected it would be very obvious how to get to the circuit.. We were in for a shock.</p>

<p>We must have walked around Verviers for 2 hours before we met someone else who had no idea where to go, his French was a little better than ours and he discovered we needed to get a bus to the circuit. At the bus stop we were comforted to see a few Ferrari caps, yes, we're in the right place.</p>

