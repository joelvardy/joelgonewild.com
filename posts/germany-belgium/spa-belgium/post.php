<p>We got to the track with time to spare, picked up our tickets, and headed into the pit lane to watch the teams rehearse some pit stops.</p>

<p>After the issues we had getting to the circuit, we really didn't need the issues we experienced to get to the camp site.. But hey that's what makes a memorable trip. We got a bus from the circuit to a small town where we thought the camp site was - it wasn't. We got some food, and picked up a few cans of beer and sat down enjoying the evening. After we'd eaten I called up the camp site and they were very helpful, they and came and picked us up :)</p>

<gallery>
    <gallery-photo path="fresh-morning"></gallery-photo>
</gallery>

<p>All travel from the camp site to the circuit was provided by the company we were camping with, however this wasn't without incident either. On the first morning before free practice the coaches simply didn't turn up, we stood around in relatively high spirits for several hours until they turned up.. We all piled on and off we went to the circuit, still in time for FP1.</p>

<p>We discovered over the course of the weekend that the weather in Spa really is as changeable as everyone says, we experienced a lot of dense morning fog, rain, cloud, and bright sun.</p>

