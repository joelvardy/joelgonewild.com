<p>Me and my brother Steve drove down to Luton pretty, about 1am to be precise. Check in was quite uneventful, and the flight (being easyJet) was adequate. However when it came to picking up our bikes, it took quite a while for us to find out they were delivered to a seemingly random baggage belt, while we were standing around like lemons at the bulky items area.</p>

<p>We got the bikes in our transfer minibus and I think I pretty much fell right to sleep. When I woke up we were in the Alpine foothills and it was very heavily raining.</p>

<p>After checking in, and chilling out, the rain had slowed, so we went for a walk, found a bar, and got a beer.</p>

