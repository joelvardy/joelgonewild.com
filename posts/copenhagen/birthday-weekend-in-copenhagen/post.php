<p>I left the office after a busy week, entering the grim January evening, and headed towards Gatwick airport having packed a change of clothes into my rucksack that morning. A pleasant flight, short train ride, and a walk across the city centre and I was at my hotel in the picturesque area of Nyhavn.</p>

<p>I woke up to a light dusting of snow on the streets outside, having been busy all week I hadn't planned my trip at all, so I decided to simply go for a walk and see what I found.</p>

<p>First up was Rosenborg Castle Gardens (literally translated: The King's Garden.) The park also contains several other historical buildings including Rosenborg Barracks, home to the Royal Guards.</p>

