<p>More exploring on the Sunday; starting with Christiansborg Palace. Located on the tiny island of Slotsholmen. It's home to the three supreme powers: the executive power, the legislative power, and the judicial power. It is the only building in the world that houses all three of a country's branches of government.</p>

