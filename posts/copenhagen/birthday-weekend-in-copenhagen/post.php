<p>I love finding vantage points in a city where you can look down on life below and out to see the cityscape, I headed up The Round Tower, the oldest functioning observatory in Europe; which instead of stairs has a spiral ramp that forms the only access to the observatory. The biggest problem facing tourists on the observation platform of this tower was that the wooden desk was covered in ice!</p>

