<p>No I wasn't doing drugs.. It's great to look down on a city from an observation deck, and Riga has several places you can do that from.</p>

<p>Leaving the hotel I headed to the Latvian Academy of Sciences observation deck, I was the only person up there leaving me to enjoy the stunning view, however despite it not being as cold today (-6°c) there is a lot more wind (wind chill of about -17°c) so I didn't spend more than half an hour on the exposed observation deck.</p>

<p>I had planned to visit the photography museum, however it was closed (I should have checked yesterday.) So after walking around old town I headed to the Skyline Bar to watch the snow fall from the 26th floor.</p>

